package com.dachen.health.pack.incomeNew.entity.vo;

public class SysSettleVO {
	
	private String month;
	private Double noSettleMoney;
	private Double settledMoney;
	private Integer status;
	
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public Double getNoSettleMoney() {
		return noSettleMoney;
	}
	public void setNoSettleMoney(Double noSettleMoney) {
		this.noSettleMoney = noSettleMoney;
	}
	public Double getSettledMoney() {
		return settledMoney;
	}
	public void setSettledMoney(Double settledMoney) {
		this.settledMoney = settledMoney;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}

}
