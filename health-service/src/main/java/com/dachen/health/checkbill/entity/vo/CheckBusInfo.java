package com.dachen.health.checkbill.entity.vo;

public class CheckBusInfo {
	
	private String productTitle;
	
	private String orderTime;
	
	private String orderId;
	
	private String checkBusDesc;
	
	private String productPrice;

	public String getProductTitle() {
		return productTitle;
	}

	public void setProductTitle(String productTitle) {
		this.productTitle = productTitle;
	}

	public String getOrderTime() {
		return orderTime;
	}

	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}


	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getCheckBusDesc() {
		return checkBusDesc;
	}

	public void setCheckBusDesc(String checkBusDesc) {
		this.checkBusDesc = checkBusDesc;
	}

	public String getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(String productPrice) {
		this.productPrice = productPrice;
	}
	
}
